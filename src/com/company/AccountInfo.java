package com.company;

public class AccountInfo {
    private Integer id;
    private String acctName;
    private String acctNo;
    private String acctType;
    private String acctLeftAmt;
    private String acctTotalAmt;
    private String acctBrNo;
    private String acctTellerNo;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getAcctName() {
        return acctName;
    }

    public void setAcctName(String acctName) {
        this.acctName = acctName;
    }

    public String getAcctNo() {
        return acctNo;
    }

    public void setAcctNo(String acctNo) {
        this.acctNo = acctNo;
    }

    public String getAcctType() {
        return acctType;
    }

    public void setAcctType(String acctType) {
        this.acctType = acctType;
    }

    public String getAcctLeftAmt() {
        return acctLeftAmt;
    }

    public void setAcctLeftAmt(String acctLeftAmt) {
        this.acctLeftAmt = acctLeftAmt;
    }

    public String getAcctTotalAmt() {
        return acctTotalAmt;
    }

    public void setAcctTotalAmt(String acctTotalAmt) {
        this.acctTotalAmt = acctTotalAmt;
    }

    public String getAcctBrNo() {
        return acctBrNo;
    }

    public void setAcctBrNo(String acctBrNo) {
        this.acctBrNo = acctBrNo;
    }

    public String getAcctTellerNo() {
        return acctTellerNo;
    }

    public void setAcctTellerNo(String acctTellerNo) {
        this.acctTellerNo = acctTellerNo;
    }
}
